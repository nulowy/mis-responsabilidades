async function dbGetAllDays() {
  const { data } = await supabase.from("daily_data").select("*").neq("day_key", "__meta__");
  if (!data) return {};
  return data.reduce((acc, row) => {
    acc[row.day_key] = JSON.parse(row.data);
    return acc;
  }, {});
}

